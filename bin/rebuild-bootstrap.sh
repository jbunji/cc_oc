#!/usr/bin/env bash
# Regenerates ~/.claude/bootstrap.md from source files.
# Sources: SOUL.md (personality), MEMORY.md (long-term memory),
#          daily/YYYY-MM-DD.md (today's note, if it exists).
# Run manually whenever SOUL.md or MEMORY.md changes.
set -euo pipefail

SOUL_DIR="/home/parallels/soul"
OUT="/home/parallels/.claude/bootstrap.md"
TODAY="$(date +%F)"
DAILY="$SOUL_DIR/daily/$TODAY.md"
RECENT="$SOUL_DIR/RECENT_WORK.md"

# Emit only the last 5 H2 sections of RECENT_WORK.md (append-only file).
recent_tail() {
  [[ -f "$RECENT" ]] || return 0
  awk '
    /^## / { n++; idx[n] = NR }
    { lines[NR] = $0 }
    END {
      if (n == 0) exit
      start = (n > 5) ? idx[n-4] : idx[1]
      for (i = start; i <= NR; i++) print lines[i]
    }
  ' "$RECENT"
}

{
  echo "<!-- GENERATED by ~/soul/bin/rebuild-bootstrap.sh on $(date -Iseconds) -->"
  echo "<!-- DO NOT HAND-EDIT. Sources: soul/SOUL.md, soul/MEMORY.md, soul/RECENT_WORK.md (last 5), soul/daily/$TODAY.md -->"
  echo
  cat "$SOUL_DIR/SOUL.md"
  echo
  cat "$SOUL_DIR/MEMORY.md"
  if [[ -f "$RECENT" ]] && [[ -s "$RECENT" ]]; then
    echo
    echo "# Recent Work (last 5 sessions)"
    recent_tail
  fi
  if [[ -f "$DAILY" ]]; then
    echo
    echo "# Today's Daily Note ($TODAY)"
    echo
    cat "$DAILY"
  fi
} > "$OUT"

echo "rebuilt $OUT ($(wc -c < "$OUT") bytes)"
